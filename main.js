document.getElementById("mudarTexto").addEventListener("click", function() {
    const poesia = document.getElementById("poesia");
    poesia.textContent = "A liberdade mora no peito, \n" +
                         "É caminhar sob o sol perfeito. \n" +
                         "Viver por si, sem prisões, \n" +
                         "Ouvir apenas as próprias razões.";
});
